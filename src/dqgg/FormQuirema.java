    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        if (this.modoForm != Modo.ALTERACAO)
            return;
        
        int opcao = JOptionPane.showConfirmDialog(this,
                                              "Confirmar exclus√£o do registro?",
                                              "CONFIRMA√á√ÉO",
                                              JOptionPane.YES_NO_OPTION);
        if (opcao == JOptionPane.YES_OPTION) 
        {
           // Pega o registro anterior ao atual
           Quirema rep = this.quirAtual.buscaAnterior(quirAtual);
           if (rep == null)
               rep = this.quirAtual.buscaProximo(quirAtual);
                      
           if (this.quirAtual.exclui()){
               JOptionPane.showMessageDialog(this,"Dados alterados com sucesso!");               
               this.atualizaFormulario(this.quirAtual=rep);
           } else {
               JOptionPane.showMessageDialog(this,"Erro durante a esclus√£o de dados!","ERRO",JOptionPane.ERROR_MESSAGE);
           }
        }
        
    }//GEN-LAST:event_btExcluirActionPerformed

